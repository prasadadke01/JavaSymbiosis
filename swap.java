class swap
{
public static void main(String args[])
{

int a = 10;
int b = 11;
System.out.println("value before swapping: " );

System.out.println("value of a is  " + a);
System.out.println("value of b is  " + b);
int c = a;
a = b;
b = c;
System.out.println("value after swapping" );
System.out.println("value of a is " + a);
System.out.println("value of b is  " + b);




}
}